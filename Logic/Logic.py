a = 5  # 이진수 : 0101 = 0000 ... 0101
b = 4  # 이진수 : 0100

print(~a) # 출력 : -6 = 1111 ....1010

print(a << 3)  # 출력 : 40
print(a >> 3)  # 출력 : 0

print(a & b)  # 출력 : 4 = 0100
print(a | b)  # 출력 : 5 = 0101
print(a ^ b)  # 출력 : 1 = 0001 (XOR)

print(1 % 5)  # 출력 : 1
print(1 / 5) # 출력 : 0.2
print(1 // 5) # 출력 : 0
